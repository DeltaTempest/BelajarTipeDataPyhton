#Stack adalah data struktur yang terbuat dari Array
#stack mempunyai aturan LIFO atau (Last In First Out) dimana yang terakhir masuk akan pertama kali keluarnya
#stack mempunyai pointer yang namanya TOP dimana fungsinya untuk melacak element yang ada di paling atas
#cara kerja stack adalah saat data ditambahkan, TOP pointer akan merujuk pada data yang masuk
#lalu saat ada data yang dimasukan kembali, data tersebut akan disimpan diatas data sebelumnya dan TOP pointer akan naik ke data baru
#saat kita mengeluarkan data paling atas, TOP pointer akan turun dan data paling atas akan keluar dari stack
